int	ft_str_is_lowercase(char *str)
{
	int	rst;
	int	i;

	rst = 1;
	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
		{
			rst = 0;
		}
		i++;
	}
	return (rst);
}
